// -------------- Valores Primitivos--------------
   // Numerico
   40

   // Cadena de Caracteres
   "Diego de Granda"

   // Booleanos
   true
   false

   // Valores vacios o place Holder
   null
   undefined

// ---------------------- Valores Tipo Objeto o No Primitivos------------

   // Valor de tipo Array
   [1, 2, 3, 4]

   // Valor tipo Objeto
   {nombre: "Diego"}

// ------------------- Como Saver que tipo de de variable es ----------

   typeof 40 // ---> "number"
   typeof "Oscar" // ---> String

   typeof true // ---> Boolean

   typeof null // ---> object
   typeof undefined // ----> undefined 

   typeof {} // ---> object
   typeof [] // ---> object

